void Invert_Led()
{
	HAL_GPIO_TogglePin(GPIOF, LED0_Pin);
	HAL_GPIO_TogglePin(GPIOF, LED1_Pin);
}

void ReadKey()
{
	static bool flag0 = 0x00;


	if(!HAL_GPIO_ReadPin(GPIOE, K0_Pin) & !flag0) flag0 = 0x01;

	if(HAL_GPIO_ReadPin(GPIOE, K0_Pin) & flag0)
	{
		Invert_Led();
		flag0 = 0x00;
	}

}

void HAL_TIM_PeriodElapsedCallback (TIM_HandleTypeDef * htim)
{
	if(htim == &htim14)
	{
		Nextion_SendInt("t0", Temp);
	  	Nextion_SendInt("t1", ADC_VAL[0]);

	  	Invert_Led();
	}


	/* HOW TO PROGRAM A STM32 Cube IDE Timer :D
	 *
	 * (clock/prescaler) = timer frequency
	 * 1/timer frequency = each tick per period of time
	 * then, the max time that I can get from a timer is:
	 * Counter value * tick period = max time
	 *
	 * Example for a 80Mhz Clock:
	 * prescaler =    80 - 1
	 * counter   = 65536 - 1
	 * 8MHz / 80 = 1MHz = 1 tick/µs
	 * Max time  = 65535 * 1µs = 65535 µs
	 *
	 * Example for a 168MHz Clock:
	 * prescaler = 8400 - 1
	 * counter   = 5000 - 1
	 * 168MHz / 8400 = 20000Mhz = 50µs
	 * Max Time = 50µs * 5000 = 250ms
	 */
}

